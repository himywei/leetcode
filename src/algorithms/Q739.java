
package algorithms;

/**
 * 请根据每日 气温 列表，重新生成一个列表。对应位置的输出为：要想观测到更高的气温，至少需要等待的天数。如果气温在这之后都不会升高，请在该位置用0 来代替。
 * <p>
 * 例如，给定一个列表temperatures = [73, 74, 75, 71, 69, 72, 76, 73]，你的输出应该是[1, 1, 4, 2, 1, 1, 0, 0]。
 * <p>
 * 提示：气温 列表长度的范围是[1, 30000]。每个气温的值的均为华氏度，都是在[30, 100]范围内的整数。
 */
public class Q739 {

    public int[] dailyTemperatures(int[] T) {
        int[] ans = new int[T.length];
        for (int i = 0; i < T.length - 1; i++) {
            if (T[i + 1] > T[i]) {
                ans[i] = 1;
            } else {
                int num = 1;
                int j = i + 1;
                while (T[j] <= T[i]) {
                    num += 1;
                    j++;
                    if (j == T.length) {
                        num = 0;
                        break;
                    }
                }
                ans[i] = num;
            }
        }
        return ans;
    }

    public static void main(String[] args) {
        int[] T = {89, 62, 70, 58, 47, 47, 46, 76, 100, 70};

        Q739 q = new Q739();
        int[] ans = q.dailyTemperatures(T);
        for (int an : ans) {
            System.out.print(an + " ");
        }

    }
}


